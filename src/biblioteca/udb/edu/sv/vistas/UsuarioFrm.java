    private void btnRestablecerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRestablecerActionPerformed
        int fila = tablaUsuarios.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un usuario primero.", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int idUsuario = (int) tablaUsuarios.getValueAt(fila, 0);

        int resp = JOptionPane.showConfirmDialog(
                this,
                "쯉eguro que desea restablecer la contrase침a a '1234'?",
                "Confirmar",
                JOptionPane.YES_NO_OPTION
        );

        if (resp == JOptionPane.YES_OPTION) {
            if (usuarioController.restablecerContrase침a(idUsuario)) {
                JOptionPane.showMessageDialog(this, "Contrase침a restablecida a '1234'.");
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo restablecer la contrase침a.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnRestablecerActionPerformed

